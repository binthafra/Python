# loops for tuple
for item in (1, 2, 3, 4, 5):
    print(item)
# loops for set
for item in {1, 2, 3, 4, 5}:
    print(item)
# loops for list
for item in [1, 2, 3, 4, 5]:
    print(item)
print(item)  # print how many items in item.not in loop

# nested loop
for item in {1, 2, 3, 4, 5}:
    for x in ['A', 'B', 'C']:
        print(item, x)
